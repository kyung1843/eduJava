package org.joonzis.Class03;

public class InfoClass {

	
	int empNo;      //(사번)
	String eName;   //(사원명)
	String job;     // (직책) 
	String hireDate; //(입사일)
	int sale;        //(급여) 
	int commission; //(성과급), 
	int deptNo;     //(부서번호)
	

	public InfoClass(int empNo, String eName, String job, String hireDate, int sale, int commission, int deptNo) {
		this.empNo = empNo;
		this.eName = eName;
		this.job = job;
		this.hireDate = hireDate;
		this.sale = sale;
		this.commission = commission;
		this.deptNo = deptNo;
	}
}
	
	
	
	
	
	

